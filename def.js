var width, height;
var sidebarOffset, rightOffset, boxWidth, boxHeight;
var img;

function preload() {
    img = loadImage("./dia/diagram-01.jpg");
}

function setup() {
    fill(255);
    textsize = 15;
    spacing = 10;

    width = windowWidth;
    height = windowHeight;
    createCanvas(width, height);
    textSize(textsize);
}

function draw() {
    background(0,0,0);
    sidebarOffset = 400;
    rightOffset = 150;
    boxWidth = width - sidebarOffset - rightOffset;
    boxHeight = height;
    text("Archival scientists describe archive as a subset of records kept for a continuing value of an institution. This is what defines my platform as an archive since it performs the same function as defined. Selectively, digital fragments generated by architects when architectural firms publicized their projects, are included in the platform. The platform acts as a subset of these metadata, discarding the unnecessary sets of digital materials. \n\nTheodore Schellenberg, of the National Archives and Records Service in Washington, was the first person to articulate a difference and division between records and archives. In the case of this platform, each of the image regarding a representation of a building, such as rendering, orthographic drawing and diagram, exists online to serve the purpose of communicating to some agency, which could be clients, professionals, students or researchers. Yet as soon as the image enters an archive, its function changes from communicative purpose to documentary purpose. This shift in function has defined the difference between records and archives", 450, 110, boxWidth, boxHeight);
    image(img, 650, 370);
    img.resize(600,300);
}